    private void tombolFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolFilterActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = new Koneksi().getConnect();

            // validasi tanggal
            if (tglDari.getDate() == null || tglSampai.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Pilih tanggal dulu!");
                return;
            }

            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String dari = sdf.format(tglDari.getDate());
            String sampai = sdf.format(tglSampai.getDate());

            String sql = "SELECT * FROM transaksi WHERE DATE(tglTransaksi) BETWEEN ? AND ?";
            java.sql.PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, dari);
            ps.setString(2, sampai);

            java.sql.ResultSet rs = ps.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tabelPendapatan.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("idTransaksi"),
                    rs.getString("namaPembeli"),
                    rs.getString("jmlBarang"),
                    rs.getString("total"),
                    rs.getDate("tglTransaksi")
                });
            }

            String sqlTotal = "SELECT SUM(total) as totalAll FROM transaksi WHERE DATE(tglTransaksi) BETWEEN ? AND ?";
            PreparedStatement psTotal = conn.prepareStatement(sqlTotal);
            psTotal.setString(1, dari);
            psTotal.setString(2, sampai);

            ResultSet rsTotal = psTotal.executeQuery();

            if (rsTotal.next()) {
                double totalAll = rsTotal.getDouble("totalAll");
                labelTotalHarga.setText(new java.text.DecimalFormat("#,##0").format(totalAll));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tombolFilterActionPerformed

