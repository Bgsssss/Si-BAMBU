    private void tombolCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolCetakActionPerformed
        // TODO add your handling code here:
        try {
            // Validasi tanggal dulu
            if (tglDari.getDate() == null || tglSampai.getDate() == null) {
                JOptionPane.showMessageDialog(null, "Pilih tanggal terlebih dahulu!");
                return;
            }

            Connection conn = new Koneksi().getConnect();

            // Kirim parameter tanggal ke Jasper
            HashMap<String, Object> parameter = new HashMap<>();
            parameter.put("tglDari", new java.sql.Date(tglDari.getDate().getTime()));
            parameter.put("tglSampai", new java.sql.Date(tglSampai.getDate().getTime()));

            System.out.println("Dari: " + tglDari.getDate());
            System.out.println("Sampai: " + tglSampai.getDate());

            InputStream is = getClass().getResourceAsStream("/laporan/laporanPendapatan.jrxml");

            if (is == null) {
                JOptionPane.showMessageDialog(null, "File laporan tidak ditemukan!");
                return;
            }

            JasperReport jr = JasperCompileManager.compileReport(is);
            JasperPrint jp = JasperFillManager.fillReport(jr, parameter, conn);
            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tombolCetakActionPerformed


